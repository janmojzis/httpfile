int base64_decode(stralloc *out, const unsigned char *in, long long inlen){

    long long i,v=0, vbits=0;
    unsigned char uch;
    char ch;

    if (!stralloc_copys(out, "")) return -1;

    for(i = 0; i < inlen; ++i){
        uch = base64values[in[i]];
        if (uch == 0xff) {
            if (in[i] == '=') continue;
            errno = EPROTO; out->len = 0; return 0;
            break;
        }
        v = (v<<6) | uch; vbits += 6;
        if (vbits >= 8) {
            ch = (v >> (vbits -= 8));
            if (!stralloc_append(out, &ch)) {out->len = 0; return -1;}
        }
    }
    return 1;
}


static const char b64chars[]="ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz0123456789+/";
static const char pad = '=';

int base64_encode(stralloc *out, const unsigned char *in, long long inlen){

    long long i,bits=0,v=0;
    char ch;

    if (!stralloc_copys(out,"")) return -1;

    for (i = 0; i < inlen; ++i) {
        v<<=8; v+=in[i]; bits+=8;
        while (bits>6) {
            ch = b64chars[((v >> (bits-6)) & 63)];
            if (!stralloc_append(out, &ch)) {out->len = 0;return -1;}
            bits-=6;
        }
    }

    if (bits) {
        v<<=(6-bits);
        ch = b64chars[v & 63];
        if (!stralloc_append(out, &ch)) {out->len = 0;return -1;}
    }
    while (out->len & 3){
        if (!stralloc_append(out, &pad)) {out->len = 0;return -1;}
    }
    return 1;
}
